/**
 * Writes config/uor-upstream.lock.json from third_party/UOR-Framework (submodule = canonical pin).
 * Do not hand-edit resolved_sha — re-run this after submodule bumps.
 */
import { execSync } from 'node:child_process';
import { writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const uor = join(root, 'third_party', 'UOR-Framework');
const configDir = join(root, 'config');
const lockPath = join(configDir, 'uor-upstream.lock.json');

if (!existsSync(join(uor, '.git'))) {
  console.error('UOR submodule missing. Run: git submodule update --init --recursive');
  process.exit(1);
}

const sha = execSync('git rev-parse HEAD', { cwd: uor, encoding: 'utf8' }).trim();

let gitRef = null;
try {
  gitRef = execSync('git describe --tags --exact-match', { cwd: uor, encoding: 'utf8' }).trim();
} catch {
  try {
    gitRef = execSync('git rev-parse --abbrev-ref HEAD', { cwd: uor, encoding: 'utf8' }).trim();
  } catch {
    gitRef = null;
  }
}

if (!gitRef || gitRef === 'HEAD') {
  try {
    gitRef = execSync('git describe --tags', { cwd: uor, encoding: 'utf8' }).trim();
  } catch {
    gitRef = sha.slice(0, 12);
  }
}

const remote = execSync('git remote get-url origin', { cwd: uor, encoding: 'utf8' }).trim();
let repository = remote.replace(/\.git$/, '');
if (repository.startsWith('git@github.com:')) {
  repository = 'https://github.com/' + repository.slice('git@github.com:'.length);
}

if (!existsSync(configDir)) {
  mkdirSync(configDir, { recursive: true });
}

const lock = {
  repository,
  git_ref: gitRef,
  resolved_sha: sha,
  ontology_exports_base: 'public/',
  notes:
    'Submodule third_party/UOR-Framework is canonical. This file is generated by npm run uor:sync-lock.',
};

writeFileSync(lockPath, JSON.stringify(lock, null, 2) + '\n', 'utf8');
console.log('Wrote config/uor-upstream.lock.json @', sha.slice(0, 7), `(${gitRef})`);
